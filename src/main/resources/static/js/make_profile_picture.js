

function make_profile_picture() {

	var picturelink1 = $('#param_pic_auto_name').val();

	 var $abc = {
				pictureNameAutoGenerated : picturelink1
			};
	


	 if (picturelink1 != null) {
			$.ajax({
				type : "POST",
				url : "/profile-picture/add",
				data : JSON.stringify($abc),
				contentType : 'application/json',

				success : function(result) {
					
					$("#make_profile_picture").html("Done");
					document.getElementById("make_profile_picture").disabled = true;
					
					alert("You Just Changed your Profile Picture");
					
				},
				error : function() {

					alert("Something Error. Post not saved.");
				}

			});
}}

